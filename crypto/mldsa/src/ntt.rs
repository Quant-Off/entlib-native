/// 상수-시간 전방 NTT (Cooley-Tukey Butterfly)
///
/// 입력 다항식 `poly`를 제자리(in-place)에서 NTT 도메인으로 변환합니다.
/// 메모리 접근 패턴과 루프 카운트는 비밀 데이터에 독립적입니다.
pub(crate) fn ntt(poly: &mut [Fq; N]) {
    let mut len = 128;
    let mut k = 1;

    // 분기 예측기를 자극하지 않는 고정된 루프 구조
    while len > 0 {
        for start in (0..N).step_by(2 * len) {
            let zeta = ZETAS[k];
            k += 1;

            for j in start..(start + len) {
                // Cooley-Tukey 나비 연산 (Butterfly)
                // t = zeta * poly[j + len]
                let t = poly[j + len].mul(zeta);

                // poly[j + len] = poly[j] - t
                poly[j + len] = poly[j].sub(t);

                // poly[j] = poly[j] + t
                poly[j] = poly[j].add(t);
            }
        }
        len >>= 1;
    }
}

/// 상수-시간 역방향 NTT (Gentleman-Sande Butterfly)
///
/// NTT 도메인의 다항식 `poly`를 제자리에서 일반 다항식 도메인으로 복원합니다.
pub(crate) fn intt(poly: &mut [Fq; N]) {
    let mut len = 1;
    let mut k = 255; // 256 - 1

    while len < N {
        for start in (0..N).step_by(2 * len) {
            let zeta = INTT_ZETAS[k];
            k -= 1;

            for j in start..(start + len) {
                let t = poly[j];

                // Gentleman-Sande 나비 연산
                // poly[j] = t + poly[j + len]
                poly[j] = t.add(poly[j + len]);

                // poly[j + len] = (t - poly[j + len]) * zeta
                let diff = t.sub(poly[j + len]);
                poly[j + len] = diff.mul(zeta);
            }
        }
        len <<= 1;
    }

    // INTT 후에는 N^-1 (256^-1) 을 모든 계수에 곱하여 스케일을 정규화해야 합니다.
    for coeff in poly.iter_mut() {
        *coeff = coeff.mul(INV_N_MONT);
    }
}
