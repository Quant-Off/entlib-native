#[test]
fn sha3_256_hash_file_self() {
    let path = std::env::current_exe().unwrap();
    let result = sha3_256::hash_file(&path);
    assert!(result.is_ok());
    assert_eq!(result.unwrap().as_slice().len(), 32);
}

#[test]
fn sha3_256_hash_file_nonexistent() {
    let result = sha3_256::hash_file("/nonexistent/path/file.bin");
    assert!(result.is_err());
}

#[test]
fn sha3_256_hash_reader_large_stream() {
    let data = vec![0x61u8; 1_000_000];
    let mut cursor = Cursor::new(&data);
    let digest = sha3_256::hash_reader(&mut cursor).unwrap();
    assert_eq!(digest.as_slice().len(), 32);
}
